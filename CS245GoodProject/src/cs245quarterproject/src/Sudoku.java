    private void SubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SubmitActionPerformed
        //Variable Declarations
        //"wrong" for future attemps
        int wrong = 0;
        
        //First time set "score"
        //Future attempts set "wrong"
        if(submitButton == false)
        {
            score = checkScore(score);
            totalScore += score;
        }
        else 
        {
            //Properly set "wrong" here because otherwise it will bypass the if statements 
            wrong = 540;
            wrong = checkScore(wrong);
        }
        
        //After first attempt switch boolean
        submitButton = true;
        
        ////////////////////////////////////////////////////////////////////////
        System.out.println("score: " + score);
        System.out.println("wrong: " + wrong);
        ////////////////////////////////////////////////////////////////////////
        
        if(score == 540 || wrong == 540)
        {
            //Go to "GameOverGui
            JFrame frame = (JFrame) SwingUtilities.getWindowAncestor(this);
            frame.getContentPane().removeAll();
            frame.repaint();

            GameOverGUI gg = new GameOverGUI(Integer.toString(totalScore));
            frame.add(gg);
            frame.setVisible(true);
        }
        else
        {
            //Centering alert based on frame position
            JFrame frame = (JFrame) SwingUtilities.getWindowAncestor(this);
            jOptionPane.showMessageDialog(frame, "Answer is incorrect please try again.");
        }
    }//GEN-LAST:event_SubmitActionPerformed

