    public int checkScore(int x)
    {
        String reference;
        
        for (int i = 0; i < fieldList.size(); i++)
        {
            reference = fieldList.get(i).getText().trim();
            if(reference.isEmpty())
            {
                x -= 10;
            }
            else if(Integer.parseInt(reference) != answers[i])
            {
                x -= 10;
            }
        }
        return x;
    }

